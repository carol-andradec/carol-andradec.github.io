function oculta1(){

  var texto;
  texto = document.getElementById('conteudo_1');
  click = document.getElementById('enlace_1');

  if(texto.style.display = "block"){
    texto.style.display = "none";
    click.innerHTML = "Mostrar Conteúdo";
  }

}


